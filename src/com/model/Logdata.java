	public String getTime1() {
		return time1;
	}
	/**
	 * @param time1 the time1 to set
	 */
	public void setTime1(String time1) {
		this.time1 = time1;
	}
	/**
	 * @return the time2
	 */
	public String getTime2() {
		return time2;
	}
	/**
	 * @param time2 the time2 to set
	 */
